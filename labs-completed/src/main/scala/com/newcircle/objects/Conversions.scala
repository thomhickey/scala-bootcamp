package com.newcircle.objects

/** Write an object called Conversions. It must contain the following
  * functions:
  *
  * - inchesToCentimeters
  * - gallonsToLiters
  * - milesToKilometers
  *
  * To eliminate floating point round-off issues, use the Scala BigDecimal
  * class. You don't need to import it.
  *
  * To save you some Googling, here are some conversion formulas:
  *
  * - 1 inch = 2.54 centimeters
  * - 1 gallon = 3.7854118 liters
  * - 1 mile = 1.609344 kilometer
  */
object Conversions {
  def inchesToCentimeters(inches: BigDecimal) = inches * 2.54
  def gallonsToLiters(gallons: BigDecimal) = gallons * 3.7854118
  def milesToKilometers(miles: BigDecimal) = miles * 1.609344
}

